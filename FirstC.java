public class FirstC {
    // Function to add two numbers
    public static int addNumbers(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        // Example usage
        int num1 = 10;
        int num2 = 20;
        int sum = addNumbers(num1, num2);

        // Print the result
        System.out.println("Sum: " + sum);
    }
}
